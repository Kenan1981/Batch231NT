package Alistirmalar;


import java.util.Scanner;

public class Alistirma_Arrays {
    public static void main(String[] args) {

        /*

         *
         * *
         * * *
         * * * *
         * * * * *
         * * * *
         * * *
         * *
         *

         */

        int sayi = 5;

        for (int i=0; i<sayi; i++){
            for (int j=0;j<=i;j++){
                System.out.print("* ");
            }
            System.out.println();
        }

        for (int k=sayi-1 ;k>=0;k--){
            for (int l=0;l<k;l++){
                System.out.print("* ");
            }

            System.out.println();
        }

        //*
        //* *
        //* * *
        //* * * *
        //* * * * *
        //* * * *
        //* * *
        //* *
        //*

















    }
}
